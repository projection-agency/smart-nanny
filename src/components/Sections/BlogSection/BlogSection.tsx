export type BlogPost = {
  id: number;
  date: string;
  slug: string;
  title: { rendered: string };
  excerpt: { rendered: string };
  featured_image_url?: string;
  categories: number[];
  // або _embedded?: { "wp:featuredmedia": [{ source_url: string }] };
};

type Category = {
  id: number;
  name: string;
};

export const BlogSection = () => {
  const [posts, setPosts] = useState<BlogPost[]>();
  const [categories, setCategories] = useState<Category[]>([]);

  useEffect(() => {
    const fetchPosts = async () => {
      try {
        const response = await fetch(`${API_URL}v2/posts`);
        const data = await response.json();
        setPosts(data);
      } catch (error) {
        console.error("Помилка при отриманні FAQ:", error);
      }
    };

    const fetchCategories = async () => {
      try {
        const response = await fetch(`${API_URL}v2/categories`);
        const data = await response.json();
        setCategories(data.filter((cat: Category) => cat.name !== "Без категорії"));
      } catch (error) {
        console.error("Помилка при отриманні категорій:", error);
      }
    };

    fetchPosts();
    fetchCategories();
  }, []);

  const getCategoryNames = (post: BlogPost) => {
    if (!post.categories || !categories.length) return [];
    return categories.filter(cat => post.categories.includes(cat.id)).map(cat => cat.name);
  };

  return (
    <section className={s.section}>
      <Container>
        <h2 className={s.title}>
          {stars}
          Щоденник сервісу <span>Новини нашої агенції{line}</span>
        </h2>

        <ul className={s.list}>
          {posts?.slice(0, 4).map((post) => (
            <BlogItem
              key={post.id}
              info={{
                title: post.title?.rendered,
                date: new Date(post.date).toLocaleDateString("uk-UA"),
                categories: getCategoryNames(post),
                image: post.featured_image_url || "/images/blog/1.jpg",
                description: post.excerpt?.rendered.replace(/<[^>]+>/g, ""),
                slug: post.slug,
              }}
            />
          ))}
        </ul>

        <Swiper
          modules={[Pagination]}
          spaceBetween={20}
          pagination={{
            type: "bullets",
            el: `.${s.paginationCont}`,
            bulletElement: "p",
          }}
          className={`${s.swiper} swiper`}
        >
          {posts?.slice(0, 4).map((post) => (
            <SwiperSlide key={post.id} className={s.swiperSlide}>
              <BlogItem
                info={{
                  title: post.title?.rendered,
                  date: new Date(post.date).toLocaleDateString("uk-UA"),
                  categories: getCategoryNames(post),
                  image: post.featured_image_url || "/images/blog/1.jpg",
                  description: post.excerpt?.rendered.replace(/<[^>]+>/g, ""),
                  slug: post.slug,
                }}
              />
            </SwiperSlide>
          ))}
        </Swiper>

        <div className={s.paginationCont}></div>

        <Link href="/blog" className={s.btn}>
          <div className={s.first}>{btnSvg}</div>
          Дивитись більше
          <div className={s.second}>{btnSvg}</div>
        </Link>
      </Container>
    </section>
  );
};

